const mongoose = require("mongoose");

const SaveCountSchema = mongoose.Schema({
  story_id: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now()
  }
});

// export model user with UserSchema
module.exports = mongoose.model("count", SaveCountSchema);
